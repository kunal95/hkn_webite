                    <p>It is not only a matter of Pride but also a moment of celebration; The Lambda Eta chapter, 

IEEE-HKN in Bharati Vidyapeeth’s College of Engineering has procured The Outstanding 

Chapter Award in its very first year of initiation. The Outstanding Chapter Award is given 

by IEEE-HKN to acknowledge excellence in colleges for its activities. To mark this event of 

honour, an award ceremony was held on 15 may 2013, Wednesday in the conference hall, 

BVCOE. Principal Sir, Prof. M. S. Bhattacharya graced the event with his humble presence. 

Prof. Rahul Bahl, Branch counsellor, BVPIEEE along with various HODs and teachers also 

dignified the ceremony with their distinguished presence. Further, all outgoing team members 

of BVPIEEE and IEEE-HKN along with the newly elected team were also present. Himshikha 

suhag hosted the ceremony.<br/>

Prof. Rahul Bahl presented Principal Sir, Prof. M.S. Bhattacharya with a bouquet as a mark 

of respect welcoming him for the ceremony. Later, Prof. M.S. Bhattacharya put forth The 

Outstanding Chapter Award Plaque to Prof. Rahul Bahl, The Branch Counsellor, BVPIEEE 

following which he addressed the audience and congratulated the team for the success and 

enthusiasm they have shown till now. Rahul Bahl Sir also reiterated the fact that initiating IEEE-
HKN in the college was not only the best foot forward but a golden foot forward. He hopes to 

get The Outstanding Chapter Award for IEEE-HKN for many years to come.<br/><br/>

To aggrandize the event, Mr Akshay Gupta, Former President, The Lambda Eta chapter IEEE-
HKN, also attended the ceremony. He has been awarded with a gold medal for the best student 

award. He has also been awarded The Outstanding Student award with special mention from 

IEEE-HKN. Apart from this, he is currently pursuing his masters in computer science at Georgia 

Tech. Moreover, he has published several papers in International Journals. Prof. Rahul Bahl 

presented Akshay Gupta with a bouquet to honour him for his achievements. To commemorate 

his success, Vishal Madan addressed him with few words of appreciation and a video clip was 

played depicting his hard work and journey towards success. Akshay Sir became nostalgic 

after watching the video. His eyes were filled with past memories and he thanked the IEEE-
HKN team for the honour and respect. Moreover, he shared his experience being IEEE-HKN 

President and wished best luck to the team to continue the good work. Lastly, Principal Sir 

awarded certificates to the outgoing IEEE-HKN team for the excellent work.</p>
