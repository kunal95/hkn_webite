			    <p>Dear IEEE-HKN Chapters,</p>

<p>At the Board of Governors meeting last November, the Board voted to raise the HKN initiation fees by $10, from $40 to $50 per initiate, effective January 1st with you directly regarding the reasons behind this increase and plans regarding possible future increases. </p>

<p>The primary reason for the increase in January is that HKN has been operating with a structural deficit for many years, and cannot continue to do this and sustain the organization. This issue was one of the motivations for the merger with IEEE, which concluded last Fall. The initiation fee has not been increased for more than a decade, lagging greatly behind inflation as many costs have increased substantially over that time including the HKN management costs as well as the costs of publishing the Bridge. The Board estimated that a target of $60 was necessary; the initiation fee increase in 2011 was the first of a multi-step process as the Board wanted to phase in the increases. At the most recent board meeting in March, the Board voted to increase the dues again in 2012 from $50 to $60, effective 1/1/2012. </p>

<p>The increases for 2011 and 2012 will only partially cover the organization's costs, and the merger with IEEE and the resulting endowment income will contribute substantially to offsetting these costs as well. We hope to hold the initiation fee at the $60 level after the increase in 2012 for as long as possible, but the Board may have to make future adjustments to the fee on an "as needed" basis. The increases for this year and next are necessary to sustain HKN, and we hope that the Chapters will understand that this increase in fees is essential to ensure the long term strength of the organization. </p>

