<p>The purpose of the Outstanding Chapter Award is to recognize excellence in college chapters for their activities. The award is based on the contents and description of chapter activities that are contained in the Annual Chapter Report. The Annual Chapter Reports are due by June 30 of each year and summarize all activities for the previous academic year from July 1 to June 30.</p>

