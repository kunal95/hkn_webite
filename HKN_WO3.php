			    <p>IEEE-Eta Kappa Nu consists of about 200 university chapters, several thousands of members, a variety of committees, many active member volunteers, and thousands of student members. </p>

<p>The overall governance of IEEE-HKN is the responsibility of the Board of Governors, a volunteer organization of IEEE-HKN members that have prominent positions in academia and industry. The Board consists of a president, vice president, past president, secretary, and treasurer, each of whom serves a one-year term. Seven directors, four representing each of IEEE-HKN's geographic regions and three at-large members, serve one three-year term. </p>

<p>IEEE-HKN Headquarters is managed by the IEEE Educational Activities Board. Nancy M. Ostin serves as the Director of IEEE-HKN. The Director is the chief operating executive of IEEE-HKN and is supported by IEEE staff members who perform the myriad of functions necessary for the success of IEEE-HKN. </p>

<p>IEEE-HKN has approximately 200 chartered chapters. Each chapter is student-led and capably guided by a faculty advisor. Students having achieved the required academic standing and also having significant leadership and exceptional character qualities are invited to become IEEE-HKN members. </p>

<p>New chapters are established upon letter request from the institution's Department Chair, Dean of Engeneering, School Cancellor, and other university administrators, and which meet the requirements of a recognized regional or professional accrediting agency in the IEEE-designated fields of interest. The IEEE-designated fields of interest include: Engineering, Computer Sciences and Information Technology, Physical Sciences, Biological and Medical Sciences, Mathematics, Technical Communications, Education,Management, Law and Policy. The chapters are supported by IEEE-HKN Headquarters and the Board of Governors' regional directors. </p>

<p>The IEEE-HKN college chapters perform a variety of service functions. Often, they help their department, college, or community. IEEE-HKN has eight standing committees consisting of dedicated volunteers. The chair of each committee is appointed by the president of the Board of Governors. Committee members are normally members of IEEE-HKN with a particular interest or talent related to the committee's purpose. IEEE-HKN encourages members to become involved by volunteering to join an IEEE-HKN committee</p>
